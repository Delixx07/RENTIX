export const fmt = (n) => 'Rp ' + Math.round(n).toLocaleString('id-ID');

// Tarif biaya (sesuai cost structure Rentix)
export const INSURANCE_RATE = 0.02; // Biaya proteksi / asuransi 2%
export const VAT_RATE = 0.11;       // PPN 11%

// Hitung rincian harga dari subtotal: asuransi 2% lalu PPN 11% atas (subtotal + asuransi).
export function priceBreakdown(subtotal) {
  const insurance = Math.round(subtotal * INSURANCE_RATE);
  const vat = Math.round((subtotal + insurance) * VAT_RATE);
  const total = subtotal + insurance + vat;
  return { subtotal, insurance, vat, total };
}

